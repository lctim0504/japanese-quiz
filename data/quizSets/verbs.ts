function createSuruItems(prefix: string, entries: [string, string][]) {
  return createWordItems(
    prefix,
    entries.map(([reading, kana]) => [`${reading}する`, `${kana}する`]),
  );
}

function createVerbCollocationItems(
  prefix: string,
  entries: readonly (readonly [string, string, string])[],
) {
  return entries.map(([reading, verb, kana], index) => ({
    id: `${prefix}-${index + 1}`,
    reading,
    kana,
    reviewLabel: `${verb}（${kana}）`,
  }));
}


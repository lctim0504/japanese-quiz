export function createPhraseCollocationItems(
  prefix: string,
  entries: [string, string, string][],
): QuizItem[] {
  return entries.map(([phrase, kana, meaning], index) => ({
    id: `${prefix}-${index + 1}`,
    reading: meaning,
    kana,
    optionLabel: phrase,
    reviewLabel: meaning,
  }));
}

export const PHRASE_COLLOCATION_ITEMS = createPhraseCollocationItems(
  "verb-phrase-collocation",
  PHRASE_COLLOCATION_ENTRIES,
);
